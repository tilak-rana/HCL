import mongoose from "mongoose";

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    trim: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
    trim: true,
  },
  tc: {
    //terms and conditions
    type: Boolean,
    required: true,
  },
});

export default mongoose.model("userSchema", userSchema, "user");
